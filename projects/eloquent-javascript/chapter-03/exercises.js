////////////////////////////////////////////////////////////////////////////////
// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(a, b) {
  if (a < b) {
    return a;
  } else {
    return b;
  }
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(n) {
  if (n === 0) { //checks to see if n(number) is equal to 0//
    return true;
  } else if (n === 1) { //checks to see if n(number) is equal to 1//
    return false;
  } else if (n < 0) { //checks to see if n is a negative number//
    return isEven(-n);
  } else {
    return isEven(n - 2);
  }
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
  let counted = 0; //starting position of the string
  for (var i = 0; i < string.length; i++) {
    if (string[i] === char) { //checks if string variable is the same as the given character
      counted += 1;
    }
  }
  return counted;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  return countChars(string, "B");
}

