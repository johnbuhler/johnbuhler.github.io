function fizzBuzz() {
for (var i = 1; i <= 15; i++) {
    var message = "";
    if (i % 3 === 0) {
        message = "fizz";
    } 
    if (i % 5 === 0) {
        message += "buzz";
    } 
    console.log(message || i);
}
}

////////////////////////////////////////////////////////////////////////////////
// drawChessboard //////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function drawChessboard(number) {
let size = number; //variable representing size of chessboard, should work for any input number
let board = ""; //empty string 

for (var y = 0; y < size; y++) {
  for (var x = 0; x < size; x++) {
    //determines whether to put a space or a # at the given position
    if ((x + y) % 2 == 0) {   
      board += " ";
    } else {
      board += "#";
    }
  }
  board += "\n";  // "\n" represents a new line
}
console.log(board);
}

